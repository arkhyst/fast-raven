    public static function isDev() { return $_ENV["STATE"] === "dev"; }
    
    public static function normalizePath(string $path): string {
        $newPath = ltrim($path, "/\\");
        $newPath = preg_replace("#[\\/]+#", "/", $newPath);
        $newPath = rtrim($newPath, "/");

        return $newPath;
    }

